package function

import kotlin.math.max
import kotlin.math.sqrt

fun main(){
    /* Standard Library Function is also known as built-in library function */

    var a : Int = max(25,35)
    println("MAX between 25 and 35 is = $a")

    var b : Double = sqrt(25.0)
    println("SquareRoot of 25 is = $b")

}
